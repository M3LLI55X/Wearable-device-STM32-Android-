package com.example.as.doctorh;

public class Tips {
    private String name;
    private int imageId;

    public Tips(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
}
